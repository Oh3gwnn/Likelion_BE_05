package javaproject2.week2.day08;

public class ArithmeticOperators1 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.printf("a + b = %d\n" , a + b);
        System.out.printf("a - b = %d\n" , a - b);
        System.out.printf("a * b = %d\n" , a * b);
        System.out.printf("a / b = %d\n" , a / b);
        System.out.printf("a %% b = %d\n" , a % b);
        // '%'를 출력하려면 '%%'처럼 한번 더 사용하면 된다.
    }
}